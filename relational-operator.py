a = 10
b = 20

# Equal to
print(a == b)  # Output: False

# Not equal to
print(a != b)  # Output: True

# Greater than
print(a > b)   # Output: False

# Less than
print(a < b)   # Output: True

# Greater than or equal to
print(a >= b)  # Output: False

# Less than or equal to
print(a <= b)  # Output: True

# Using relational operators in an if statement
if a < b:
    print(f"{a} is less than {b}")
else:
    print(f"{a} is not less than {b}")
