# len()
# Returns the length of a string

string = "Hello, World!"
print(len(string))  # Output: 13


string = "Hello"
print(string.upper())  # Output: "HELLO"
print(string.lower())  # Output: "hello"


string = "Hello, World!"
print(string.split(", "))  # Output: ['Hello', 'World!']


words = ['Hello', 'World']
print(" ".join(words))  # Output: "Hello World"
